public class Pgm0304 {
	public static void main(String[] argv) {
		char c;
		int i;

		c = '\141'; // 8進位141為97
		i = c;
		System.out.println("字碼：" + i + "的字元為 " + c);

		c = '\142'; // 8進位142為98
		i = c;
		System.out.println("字碼：" + i + "的字元為 " + c);

		c = '\143'; // 8進位143為99
		i = c;
		System.out.println("字碼：" + i + "的字元為 " + c);
	}
}